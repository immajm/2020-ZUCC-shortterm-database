package takeout.model;

public class BeanFull {
	private int full_id;
	private int order_id;
	private double full_reduction;
	private double full_discount_amount;
	private int tag;
	
	public int getFull_id() {
		return full_id;
	}
	public void setFull_id(int full_id) {
		this.full_id = full_id;
	}
	public int getOrder_id() {
		return order_id;
	}
	public void setOrder_id(int order_id) {
		this.order_id = order_id;
	}
	public double getFull_reduction() {
		return full_reduction;
	}
	public void setFull_reduction(double full_reduction) {
		this.full_reduction = full_reduction;
	}
	public double getFull_discount_amount() {
		return full_discount_amount;
	}
	public void setFull_discount_amount(double full_discount_amount) {
		this.full_discount_amount = full_discount_amount;
	}
	public int getTag() {
		return tag;
	}
	public void setTag(int tag) {
		this.tag = tag;
	}
	
	
	
	
}
